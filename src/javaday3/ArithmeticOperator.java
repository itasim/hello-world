package javaday3;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int intResult = 0;
		double doubleResult = 0;

		intResult = 3 + 2;
		System.out.println(intResult);

		intResult = 3 - 2;
		System.out.println(intResult);

		intResult = 3 * 2;
		System.out.println(intResult);

		intResult = 3 / 2;
		System.out.println(intResult);

		doubleResult = 3.0 / 2.0;
		System.out.println(doubleResult);

		intResult = 3 % 2;
		System.out.println(intResult);

	}

}
